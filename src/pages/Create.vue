import { rolesArray } from '../config/userRolesConfig';
export default {
    name: 'Create',
    data() {
        return {
            referredBusinessName: '',
            firstName: '',
            lastName: '',
            email: '',
            phoneNumber: '',
            userRole: '',
            password: '',
            confirmPassword: '',
            error: '',
            isLoading: false,
            roleOptions: [{
                label: 'Select a role',
                value: ''
            }, ...rolesArray],
        }
    },
    methods: {
        async register() {
            this.error = '';
            this.isLoading = true;

            if (this.password !== this.confirmPassword) {
                this.error = "Passwords do not match.";
                this.isLoading = false;
                return;
            }

            if (!this.userRole) {
                this.error = "User role is missing.";
                this.isLoading = false;
                return;
            }

            // Simulate registration without Firebase
            setTimeout(() => {
                const mockUid = 'user_' + Date.now();
                const userData = {
                    referredBusinessName: this.referredBusinessName,
                    firstName: this.firstName,
                    lastName: this.lastName,
                    phoneNumber: this.phoneNumber,
                    userRole: this.userRole,
                    email: this.email,
                    createdAt: new Date()
                };

                this.$store.dispatch('login', { ...userData, uid: mockUid });
                this.isLoading = false;
                console.log('User registered:', mockUid);
                this.$router.push('/').catch(()=>{});
            }, 500);
        }
    }
}
</script>

